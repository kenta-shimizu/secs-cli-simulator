package com.shimizukenta.secssimulator;

public class SecsSimulatorException extends Exception {
	
	private static final long serialVersionUID = 2542547717662596111L;
	
	public SecsSimulatorException() {
		super();
	}

	public SecsSimulatorException(String message) {
		super(message);
	}

	public SecsSimulatorException(Throwable cause) {
		super(cause);
	}

	public SecsSimulatorException(String message, Throwable cause) {
		super(message, cause);
	}
	
}
