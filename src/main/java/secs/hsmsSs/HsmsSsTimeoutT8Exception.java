package secs.hsmsSs;

import secs.SecsException;

public class HsmsSsTimeoutT8Exception extends SecsException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2734543965084377328L;

	public HsmsSsTimeoutT8Exception() {
		super();
	}

	public HsmsSsTimeoutT8Exception(String message) {
		super(message);
	}

	public HsmsSsTimeoutT8Exception(Throwable cause) {
		super(cause);
	}

	public HsmsSsTimeoutT8Exception(String message, Throwable cause) {
		super(message, cause);
	}

	public HsmsSsTimeoutT8Exception(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
