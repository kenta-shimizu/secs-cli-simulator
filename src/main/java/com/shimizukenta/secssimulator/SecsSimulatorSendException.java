package com.shimizukenta.secssimulator;

public class SecsSimulatorSendException extends SecsSimulatorException {

	private static final long serialVersionUID = 5527778107643637188L;

	public SecsSimulatorSendException() {
		super();
	}

	public SecsSimulatorSendException(String message) {
		super(message);
	}

	public SecsSimulatorSendException(Throwable cause) {
		super(cause);
	}

	public SecsSimulatorSendException(String message, Throwable cause) {
		super(message, cause);
	}

}
